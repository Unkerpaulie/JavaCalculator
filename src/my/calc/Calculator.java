    private void calculateResult(){
        txtFormula.setText(txtFormula.getText() + " " + txtDisplay.getText());
        txtDisplay.setText("");
        String formula = txtFormula.getText();
        txtFormula.setText(txtFormula.getText() + " =");
        // replace 'Ã·' with / and 'Ã—' with *
        formula = formula.replace('Ã·', '/');
        formula = formula.replace('Ã—', '*');
        
        evaluateFormulaJS(formula);
    }
    
    private void evaluateFormulaJS(String formula){
        // since javascript can't evaluate arithmetic expressions from strings, we need javascript to help us
        try {
            ScriptEngineManager manager = new ScriptEngineManager();
            ScriptEngine engine = manager.getEngineByName("JavaScript");        
            Object result = engine.eval(formula);
            if (result.toString().length() > maxChars){
                statusMessage("Result exceeds the display size");
            }
            txtDisplay.setText(result.toString().substring(0, Math.min(result.toString().length(), maxChars)));
        } catch(ScriptException e) {
            // handle exception
            System.err.println("Error evaluating the equation: " + e.getMessage());
            statusMessage("There was an error evaluating the equation");
        }
    }
    
