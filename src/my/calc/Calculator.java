    private void pressNum(String num){
        clearStatus();
        // clear everything if last operation was =
        if (lastCharInFormula() == '='){
            statusMessage("New calculation");
            clearAll();
        }
        // append pressed number to display, up to maxWidth
        if (txtDisplay.getText().length() < maxChars){
            txtDisplay.setText(txtDisplay.getText() + num);
        } else {
            statusMessage("Display full");
        }
    }
    
    private boolean lastCharIsOper(){
        // returns true if the last character in formula is an operator
        char[] operList = {'+', '-', 'Ã·', 'Ã—'};
        boolean isOper = false;
        // checking if last char in formula window is an operation char
        if (!txtFormula.getText().equals("")) {
            for (char o: operList) {
                if (lastCharInFormula() == o) {
                    isOper = true;
                }
            }
        }
        return isOper;
    }
    
    private char lastCharInFormula(){
        if (!txtFormula.getText().equals("")){
            return txtFormula.getText().charAt(txtFormula.getText().length()-1);
        } else {
            return 0;
        }
    }
    
    private void pressOper(String oper){
        clearStatus();
        // method for registering operation entry

        if (txtFormula.getText().equals("")) {
            // formula contains nothing
            if (txtDisplay.getText().equals("")){
                // display clear, only operation acceptable is -
                if (oper.equals("-")) {
                    pressNum("-");
                } else {
                    statusMessage("Enter a number before an operator");
                }
            } else {
                // display contains a number
                enterOper(oper);
            }
        } else {
            // formula contains something
            if (txtDisplay.getText().equals("") && lastCharIsOper()){
                // an operator button was previously pressed, replace it with current operation
                String cutOper = txtFormula.getText().substring(0, txtFormula.getText().length() - 1);
                txtFormula.setText(cutOper + oper);
                statusMessage("Operation changed");
            } else if (lastCharInFormula() == '='){
                // equal was last pressed, result in display, continue with operation
                txtFormula.setText(txtDisplay.getText() + " " + oper);
                txtDisplay.setText("");
                statusMessage("Continuing calculation...");
            } else {
                // number in display, proceed as normal
                enterOper(oper);
            }
        }
    }
    
    private void enterOper(String oper){
        // add input to formula window and clear display
        txtFormula.setText(txtFormula.getText() + " " + txtDisplay.getText() + " " + oper);
        txtDisplay.setText("");
    }
    
